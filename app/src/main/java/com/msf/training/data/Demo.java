package com.msf.training.data;

public class Demo {
    private String local_var;
    private String instance_var;

    public Demo(String local_var, String instance_var) {
        this.local_var = local_var;
        this.instance_var = instance_var;
    }

    public String getLocal_var() {
        return local_var;
    }

    public void setLocal_var(String local_var) {
        this.local_var = local_var;
    }

    public String getInstance_var() {
        return instance_var;
    }

    public void setInstance_var(String instance_var) {
        this.instance_var = instance_var;
    }
}
